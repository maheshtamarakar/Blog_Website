def user_register(request):
    if request.method == 'POST':
        fname=request.POST.get('fname')
        lname=request.POST.get('lname')
        uname=request.POST.get('uname')
        email=request.POST.get('email')
        pass1=request.POST.get('password')
        pass2=request.POST.get('password2')
        print(fname, lname, uname, email, pass1, pass2)
        
        if pass1!=pass2:
            messages.warning(request, 'Your Password Does not match! Try Again')
            return redirect('register')
        elif User.objects.filter(username=uname).exists():
            messages.warning(request, 'This Username is already exist! Try Different.')
            return redirect('register')
        elif User.objects.filter(email=email).exists():
            messages.warning(request, 'This Email is already taken!.')
            return redirect('register')
        else:
            user = User.objects.create_user(first_name=fname, last_name=lname, email=email, password=pass1, username=uname)
            user.save()
            messages.success(request, 'Your Registration has been completed!')
            return redirect('/')
     
    return render(request,'register.html')
def user_login(request):
    if request.method=='POST':
    
        username=request.POST.get('luname')
        password=request.POST.get('lpassword')
        # print(username, password)
        user = authenticate(request, username=username, password=password)
        if user is not None:
            login(request, user)
            # Redirect to a success page.
            messages.success(request, 'You are loged in successfully!')
            return redirect('/')
        else:
            # Return an 'invalid login' error message.
            messages.warning(request, 'Invalid Credential')
            return redirect('login')
    return render(request,'login.html')

def logout_view(request):

    logout(request)
    messages.success(request, 'You are loged Out successfully!')
    return redirect('/')

def post_blog(request):
    if request.method=='POST':
        title=request.POST.get('title')
        desc=request.POST.get('description')
        Blog=blog(title=title, dsc=desc, user_id=request.user)
        Blog.save()
        messages.success(request,'Post has been submitted successfully')
        return redirect('post_blog')
    return render(request, 'post_blog.html')

def blog_detail(request, id):
    Blog= blog.objects.get(id=id)#collect id like 2 or 3 auto generated by django perticular value, don't need to use for loop bcoz there is only one value
