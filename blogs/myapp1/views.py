def delete(request, id):
    Blog= blog.objects.get(id=id)
    Blog.delete()
    messages.success(request, 'Post has been deleted successfully')
    return redirect('/')

def edit(request, id):
    Blog= blog.objects.get(id=id)
    edit_blog= Edit_Blog(instance=Blog)#instance = Blog will give already written text of writer to my edit page 
    if request.method=='POST':
        form = Edit_Blog(request.POST, instance=Blog)#instance = Blog so django will know i want to do some changes on it
        if form.is_valid():
            form.save()
            messages.success(request, 'Post has been Updated successfully')
            return redirect('/')

    return render(request, 'edit_blog.html', {'edit_blog':edit_blog})